package student_eduards_puzirevskis.lesson_6_arrays_while_loop.level_5_6;

 class Move {

     private int x;
     private int y;

     public Move(int x, int y) {
         this.x = x;
         this.y = y;
     }

     public int getX() {
         return x;
     }

     public int getY() {
         return y;
     }
 }
